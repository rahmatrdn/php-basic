<?php
$a = 10;
$b = 20;

// Operator Sama Dengan (==)
if ($a == $b) {
    echo "\$a sama dengan \$b<br>";
} else {
    echo "\$a tidak sama dengan \$b<br>";
}

// Operator Tidak Sama Dengan (!=)
if ($a != $b) {
    echo "\$a tidak sama dengan \$b<br>";
}

// Operator Lebih Besar (>)
if ($a > $b) {
    echo "\$a lebih besar dari \$b<br>";
} else {
    echo "\$a tidak lebih besar dari \$b<br>";
}

// Operator Lebih Kecil (<)
if ($a < $b) {
    echo "\$a lebih kecil dari \$b<br>";
}

// Operator Lebih Besar atau Sama Dengan (>=)
if ($a >= $b) {
    echo "\$a lebih besar atau sama dengan \$b<br>";
} else {
    echo "\$a tidak lebih besar atau sama dengan \$b<br>";
}

// Operator Lebih Kecil atau Sama Dengan (<=)
if ($a <= $b) {
    echo "\$a lebih kecil atau sama dengan \$b<br>";
}

